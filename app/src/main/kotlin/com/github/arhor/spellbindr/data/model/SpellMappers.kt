package com.github.arhor.spellbindr.data.model

import com.github.arhor.spellbindr.domain.model.Spell as DomainSpell

fun Spell.toDomain(): DomainSpell = DomainSpell(
    id = id,
    name = name,
    desc = desc,
    level = level,
    range = range,
    ritual = ritual,
    school = school,
    duration = duration,
    castingTime = castingTime,
    classes = classes,
    components = components,
    concentration = concentration,
    areaOfEffect = areaOfEffect,
    attackType = attackType,
    damage = damage,
    dc = dc,
    healAtSlotLevel = healAtSlotLevel,
    higherLevel = higherLevel,
    material = material,
    subclasses = subclasses,
    source = source,
)

fun DomainSpell.toData(): Spell = Spell(
    id = id,
    name = name,
    desc = desc,
    level = level,
    range = range,
    ritual = ritual,
    school = school,
    duration = duration,
    castingTime = castingTime,
    classes = classes,
    components = components,
    concentration = concentration,
    areaOfEffect = areaOfEffect,
    attackType = attackType,
    damage = damage,
    dc = dc,
    healAtSlotLevel = healAtSlotLevel,
    higherLevel = higherLevel,
    material = material,
    subclasses = subclasses,
    source = source,
)
